class Content {
  String url;
  String tagLine;
  String userName;
  String timeSinceUpload;

  Content(
    String url,
    String tagLine,
    String userName,
    String timeSinceUpload,
  ) {
    this.url=url;
    this.tagLine=tagLine;
    this.userName=userName;
    this.timeSinceUpload=timeSinceUpload;
  }
}
